// Helper Functions
export const getEventById = (id) => {
  return mockEvents.find(event => event.id === parseInt(id));
};

export const getEventsByCompany = (companyId) => {
  return mockEvents.filter(event => event.companyId === companyId);
};

export const getEventsByStatus = (status) => {
  return mockEvents.filter(event => event.status === status);
};

export const getFeaturedEvents = () => {
  return mockEvents.filter(event => event.featured && event.status === 'published');
};

export const getPublishedEvents = () => {
  return mockEvents.filter(event => event.status === 'published');
};

export const getUserById = (id) => {
  return mockUsers.find(user => user.id === parseInt(id));
};

export const getCompaniesByStatus = (status) => {
  return mockUsers.filter(user => user.role === 'company' && user.status === status);
};

export const formatDate = (dateString) => {
  const date = new Date(dateString);
  return date.toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric'
  });
};

export const getEventStatus = (event) => {
  const eventDate = new Date(event.date);
  const today = new Date();

  if (eventDate < today) {
    return 'past';
  } else if (event.registered >= event.capacity) {
    return 'full';
  } else {
    return 'upcoming';
  }
};

export const calculateProgress = (registered, capacity) => {
  return Math.round((registered / capacity) * 100);
};

